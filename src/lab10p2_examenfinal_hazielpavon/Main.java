    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        int edad = Integer.parseInt(Ds_edad.getText());

        String tipo = (String) Ds_Tipo.getSelectedItem();

        Date h = new Date();
        DateFormat df = new SimpleDateFormat("yyyy/MM/dd/hh:mm");
        String x = df.format(h);

        if (edad < 12) {
            JOptionPane.showMessageDialog(jPanel1, "Debe ser mayor de 12 a√±os para registrarse como cliente.");
        } else if (tipo.equals("Artista") && edad < 18) {

            JOptionPane.showMessageDialog(jPanel1, "Debe ser mayor de 18 a√±os para registrarse como artista.");

        } else {

            String contrax = Ds_Contracrear.getText();

            String usua = Ds_Nombrecrear.getText();

            contra.add(contrax);

            nombre.add(usua);

            Contrayusuario n = new Contrayusuario(usua, contrax, x, edad);

            JOptionPane.showMessageDialog(jPanel1, "Se ha guardado exitosamente");

            JFileChooser jfc = new JFileChooser();
            FileNameExtensionFilter filtro
                    = new FileNameExtensionFilter(
                            "Archivos de Texto", "txt");
            jfc.addChoosableFileFilter(filtro);
            int seleccion = jfc.showSaveDialog(this);
            FileWriter fw = null;
            BufferedWriter bw = null;
            if (seleccion == JFileChooser.APPROVE_OPTION) {
                try {

                    File fichero = null;
                    if (jfc.getFileFilter().getDescription().equals(
                            "Archivos de Texto")) {
                        fichero
                                = new File(jfc.getSelectedFile().getPath() + ".txt");
                    } else {
                        fichero = jfc.getSelectedFile();
                    }
                    fw = new FileWriter(fichero, true);
                    bw = new BufferedWriter(fw);

                    bw.write("Bitacora" + "\n");

                    if (contra.size() == 0 && nombre.size() == 0) {

                    } else {

                        bw.write(n.toString());
                        bw.write("\n");
                    }
                    bw.flush();
                    JOptionPane.showMessageDialog(this,
                            "Archivo guardado exitosamente");

                } catch (Exception e) {
                    e.printStackTrace();
                }
                try {
                    bw.close();
                    fw.close();
                } catch (IOException ex) {
                }
            }
        }

        Ds_Nombrecrear.setText(
                "");
        Ds_Contracrear.setText(
                "");
        Ds_edad.setText(
                "");
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked

        String nombrex = Ds_nombre.getText();
        String usuario = Ds_usuario.getText();
        boolean encon = false;

        for (int i = 0; i < contra.size(); i++) {
            if (contra.get(i).equals(usuario)) {
                encon = true;
                break;
            }
        }

        for (int i = 0; i < nombre.size(); i++) {
            if (nombre.get(i).equals(nombrex)) {
                encon = true;
                break;
            }
        }

        if (!encon) {
            JOptionPane.showMessageDialog(jPanel1, "Contrase√±a o usuario incorrectos, si no ha creado un usuario, vaya y cr√©elo");
        } else {
            JOptionPane.showMessageDialog(jPanel1, "Ha ingresado");

            if (Ds_Tipo.getSelectedItem().equals("Artista")) {
                Artista.pack();
                Artista.setModal(true);
                Artista.setLocationRelativeTo(this);
                Artista.setVisible(true);
            } else {
                spotify.pack();
                spotify.setModal(true);
                spotify.setLocationRelativeTo(this);
                spotify.setVisible(true);
            }

            Ds_nombre.setText("");
            Ds_usuario.setText("");
        }

    }//GEN-LAST:event_jButton2MouseClicked

